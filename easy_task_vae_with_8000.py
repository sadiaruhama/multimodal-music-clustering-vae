"""## 4. VAE architecture"""

class Encoder(nn.Module):
    def __init__(self, input_dim, hidden_dims, latent_dim):
        super().__init__()
        layers = []
        prev = input_dim
        for h in hidden_dims:
            layers += [nn.Linear(prev, h), nn.BatchNorm1d(h), nn.LeakyReLU(0.2)]
            prev = h
        self.net  = nn.Sequential(*layers)
        self.mu   = nn.Linear(prev, latent_dim)
        self.logv = nn.Linear(prev, latent_dim)

    def forward(self, x):
        h = self.net(x)
        return self.mu(h), self.logv(h)


class Decoder(nn.Module):
    def __init__(self, latent_dim, hidden_dims, output_dim):
        super().__init__()
        layers = []
        prev = latent_dim
        for h in reversed(hidden_dims):
            layers += [nn.Linear(prev, h), nn.BatchNorm1d(h), nn.LeakyReLU(0.2)]
            prev = h
        layers.append(nn.Linear(prev, output_dim))
        self.net = nn.Sequential(*layers)

    def forward(self, z):
        return self.net(z)


class VAE(nn.Module):
    def __init__(self, input_dim=80, hidden_dims=(256, 128), latent_dim=32):
        super().__init__()
        self.encoder = Encoder(input_dim, hidden_dims, latent_dim)
        self.decoder = Decoder(latent_dim, hidden_dims, input_dim)
        self.latent_dim = latent_dim

    def reparameterise(self, mu, logv):
        std = torch.exp(0.5 * logv)
        eps = torch.randn_like(std)
        return mu + eps * std

    def forward(self, x):
        mu, logv = self.encoder(x)
        z        = self.reparameterise(mu, logv)
        x_recon  = self.decoder(z)
        return x_recon, mu, logv

    def encode(self, x):
        """Return mu only (deterministic latent representation)."""
        mu, _ = self.encoder(x)
        return mu


def vae_loss(x, x_recon, mu, logv, beta=1.0):
    """ELBO = MSE reconstruction + beta * KL divergence."""
    recon_loss = nn.functional.mse_loss(x_recon, x, reduction='mean')
    kl_loss    = -0.5 * torch.mean(1 + logv - mu.pow(2) - logv.exp())
    return recon_loss + beta * kl_loss, recon_loss, kl_loss


