"""## 5. Train the VAE"""

# ── Hyperparameters ──
EPOCHS     = 100
BATCH_SIZE = 256
LR         = 1e-3
BETA       = 1.0   # standard VAE; increase for Beta-VAE in Hard Task

# ── DataLoader — train on full hybrid dataset ──
X_tensor   = torch.tensor(X_all, dtype=torch.float32)
dataset    = TensorDataset(X_tensor)
loader     = DataLoader(dataset, batch_size=BATCH_SIZE, shuffle=True,
                        num_workers=0, pin_memory=True)

optimizer  = optim.Adam(model.parameters(), lr=LR, weight_decay=1e-5)
scheduler  = optim.lr_scheduler.CosineAnnealingLR(optimizer, T_max=EPOCHS)

# ── Training loop ──
history = {'total': [], 'recon': [], 'kl': []}

model.train()
for epoch in range(1, EPOCHS + 1):
    ep_total = ep_recon = ep_kl = 0.0
    for (xb,) in loader:
        xb = xb.to(device)
        optimizer.zero_grad()
        x_recon, mu, logv = model(xb)
        loss, recon, kl   = vae_loss(xb, x_recon, mu, logv, beta=BETA)
        loss.backward()
        nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        optimizer.step()
        ep_total += loss.item()
        ep_recon += recon.item()
        ep_kl    += kl.item()
    scheduler.step()

    n = len(loader)
    history['total'].append(ep_total / n)
    history['recon'].append(ep_recon / n)
    history['kl'].append(ep_kl / n)

    if epoch % 10 == 0 or epoch == 1:
        print(f'Epoch {epoch:3d}/{EPOCHS} | '
              f'Total: {history["total"][-1]:.4f} | '
              f'Recon: {history["recon"][-1]:.4f} | '
              f'KL: {history["kl"][-1]:.4f}')

