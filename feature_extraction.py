"""Extract mel-spectrograms (for Conv-VAE in medium task)"""

import librosa
from tqdm import tqdm

SPEC_DIR  = 'data/spectrograms'
N_MELS    = 128
HOP_LEN   = 512
TARGET_LEN = 128

spec_records = []

print(f"Extracting mel-spectrograms from {len(gtzan_df)} tracks...")

for idx, row in tqdm(gtzan_df.iterrows(), total=len(gtzan_df)):
    filepath = row['filepath']
    filename = row['filename']
    genre    = row['genre']

    try:
        y, sr = librosa.load(filepath, sr=SR, duration=DURATION)

        # Mel-spectrogram — shape (128, T)
        mel = librosa.feature.melspectrogram(
            y=y, sr=sr,
            n_mels=N_MELS,
            hop_length=HOP_LEN
        )

        # Convert to dB scale
        mel_db = librosa.power_to_db(mel, ref=np.max)

        # Fix time axis to TARGET_LEN (128) for consistent input shape
        if mel_db.shape[1] >= TARGET_LEN:
            mel_fixed = mel_db[:, :TARGET_LEN]
        else:
            # Pad with minimum value if shorter
            pad_width = TARGET_LEN - mel_db.shape[1]
            mel_fixed = np.pad(mel_db, ((0,0),(0,pad_width)), mode='minimum')

        # Final shape: (128, 128) — suitable for 2D Conv layers
        save_name = filename.replace('.au', '.npy')
        save_path = os.path.join(SPEC_DIR, save_name)
        np.save(save_path, mel_fixed)

        spec_records.append({
            'filename'  : filename,
            'genre'     : genre,
            'spec_path' : save_path,
            'shape'     : mel_fixed.shape
        })

    except Exception as e:
        print(f"Error on {filename}: {e}")

print(f"\nSuccessfully extracted: {len(spec_records)}")

spec_df = pd.DataFrame(spec_records)
spec_df.to_csv('data/spec_index.csv', index=False)
print("Spectrogram index saved.")


