"""Extract MFCC features from all 1000 tracks"""

import librosa
import numpy as np
from tqdm import tqdm

MFCC_DIR = 'data/mfcc'
N_MFCC   = 40
SR       = 22050
DURATION = 30  # seconds

mfcc_records = []
failed       = []

print(f"Extracting MFCCs from {len(gtzan_df)} tracks...")

for idx, row in tqdm(gtzan_df.iterrows(), total=len(gtzan_df)):
    filepath = row['filepath']
    filename = row['filename']
    genre    = row['genre']

    try:

        y, sr = librosa.load(filepath, sr=SR, duration=DURATION)

        # Extract MFCC — shape (40, T)
        mfcc = librosa.feature.mfcc(y=y, sr=sr, n_mfcc=N_MFCC)

        # Take mean across time → shape (40,) — one vector per track
        mfcc_mean = mfcc.mean(axis=1)

        # Also store std for richer features
        mfcc_std  = mfcc.std(axis=1)

        # Concatenate mean + std → shape (80,)
        mfcc_combined = np.concatenate([mfcc_mean, mfcc_std])


        save_name = filename.replace('.au', '.npy')
        save_path = os.path.join(MFCC_DIR, save_name)
        np.save(save_path, mfcc_combined)

        mfcc_records.append({
            'filename'  : filename,
            'genre'     : genre,
            'mfcc_path' : save_path,
            'filepath'  : filepath
        })

    except Exception as e:
        failed.append({'filename': filename, 'error': str(e)})

print(f"\nSuccessfully extracted: {len(mfcc_records)}")
print(f"Failed                : {len(failed)}")
if failed:
    print("Failed files:", [f['filename'] for f in failed])

# Save MFCC index
mfcc_df = pd.DataFrame(mfcc_records)
