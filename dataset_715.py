"""Bollywood Dataset Cleaning"""

import ast
import re

def parse_lyrics_column(raw_text):
    if pd.isna(raw_text):
        return None
    try:
        lines = ast.literal_eval(raw_text)
        if isinstance(lines, list) and len(lines) > 1:
            lines = lines[1:]  # skip 'Lyrics' or 'Translation' header
        text = ' '.join([l.strip() for l in lines if l.strip()])
        return text
    except Exception:
        return str(raw_text).strip()

def clean_text(text):
    if not text:
        return None
    text = re.sub(r'\s+', ' ', text)
    text = text.strip()
    return text if len(text) > 20 else None

df = df_raw.copy()


df = df.rename(columns={
    'Unnamed: 0': 'index',
    'Song Title': 'song_title',
    'Hindi Lyrics': 'hindi_lyrics',
    'English Translations': 'english_translation'
})
df = df.drop(columns=['index'])

print("Before cleaning:", len(df), "rows")

df['hindi_lyrics_clean']   = df['hindi_lyrics'].apply(parse_lyrics_column).apply(clean_text)
df['english_lyrics_clean'] = df['english_translation'].apply(parse_lyrics_column).apply(clean_text)

df = df.dropna(subset=['hindi_lyrics_clean', 'english_lyrics_clean'], how='all')
df = df.drop_duplicates(subset=['song_title'])
df = df.reset_index(drop=True)

print("After cleaning:", len(df), "rows")
print("\nSample Hindi lyrics (cleaned):")
print(df['hindi_lyrics_clean'].iloc[0][:200])
print("\nSample English translation (cleaned):")
