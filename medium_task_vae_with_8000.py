# BUG-7 FIX: The original code created y_int once at shape (1000,) and reused
# it for every evaluate_clustering call. If BUG-1 reduced the dataset size (e.g.
# to 990 tracks), y_int was still length 1000 and the slice t_m = true_labels[mask]
# would index out of bounds or silently misalign for DBSCAN-masked subsets.
