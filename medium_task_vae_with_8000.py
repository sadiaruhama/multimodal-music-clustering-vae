# FIX: Pass y_genre (string labels) directly to evaluate_clustering and let the
# function re-encode them with LabelEncoder internally after applying any noise
# mask. This guarantees t_int always matches Z_m and p_m exactly.

def evaluate_clustering(Z, pred_labels, true_labels, name, skip_noise=True):
    """
    Compute Silhouette, Davies-Bouldin, and ARI.
    - Noise points (-1) from DBSCAN are excluded.
    - true_labels can be strings or ints; re-encoded internally per subset.
    """
    pred_labels = np.asarray(pred_labels)
    true_labels = np.asarray(true_labels)

    if skip_noise and -1 in pred_labels:
        mask        = pred_labels != -1
        Z_m         = Z[mask]
        p_m         = pred_labels[mask]
        t_m         = true_labels[mask]
        note        = f'(excl. {(~mask).sum()} noise points)'
    else:
        Z_m, p_m, t_m, note = Z, pred_labels, true_labels, ''

    # Re-encode true labels to ints (safe for any subset size or label type)
    t_int    = LabelEncoder().fit_transform(t_m)
    n_unique = len(np.unique(p_m))

    if n_unique < 2:
        print(f'  {name:45s}  SKIP — only {n_unique} cluster(s); check for degenerate latents')
        return {
            'method': name, 'n_clusters': n_unique,
            'silhouette': np.nan, 'davies_bouldin': np.nan,
            'ARI': np.nan, 'note': 'too few clusters',
        }

    sil = silhouette_score(Z_m, p_m, sample_size=min(2000, len(Z_m)), random_state=SEED)
    db  = davies_bouldin_score(Z_m, p_m)
    ari = adjusted_rand_score(t_int, p_m)

    print(f'  {name:45s}  Sil={sil:.4f}  DB={db:.4f}  ARI={ari:.4f}  {note}')
    return {
        'method'        : name,
        'n_clusters'    : n_unique,
        'silhouette'    : round(sil, 4),
        'davies_bouldin': round(db,  4),
        'ARI'           : round(ari, 4),
        'note'          : note,
    }


