def cluster_purity(true_labels, pred_labels):
    """Fraction of tracks in the dominant true class per cluster."""
    total = 0
    n     = len(true_labels)
    for cluster_id in np.unique(pred_labels):
        if cluster_id == -1:
            continue
        mask   = pred_labels == cluster_id
        counts = np.bincount(LabelEncoder().fit_transform(true_labels[mask]))
        total += counts.max()
    return total / n

