# ── 4. Conv-VAE architecture ──────────────────────────────────────────────────

class ConvEncoder(nn.Module):
    """4-layer conv encoder: 128→64→32→16→8 spatial, 1→32→64→128→256 channels."""
    def __init__(self, latent_dim=32):
        super().__init__()
        self.conv = nn.Sequential(
            nn.Conv2d(1,   32,  kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(32),  nn.LeakyReLU(0.2),
            nn.Conv2d(32,  64,  kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(64),  nn.LeakyReLU(0.2),
            nn.Conv2d(64,  128, kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(128), nn.LeakyReLU(0.2),
            nn.Conv2d(128, 256, kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(256), nn.LeakyReLU(0.2),
        )
        self.fc_mu   = nn.Linear(256 * 8 * 8, latent_dim)
        self.fc_logv = nn.Linear(256 * 8 * 8, latent_dim)

    def forward(self, x):
        h = self.conv(x).view(x.size(0), -1)
        return self.fc_mu(h), self.fc_logv(h)


class ConvDecoder(nn.Module):
    """Mirror of ConvEncoder using transposed convolutions."""
    def __init__(self, latent_dim=32):
        super().__init__()
        self.fc = nn.Linear(latent_dim, 256 * 8 * 8)
        self.deconv = nn.Sequential(
            nn.ConvTranspose2d(256, 128, kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(128), nn.LeakyReLU(0.2),
            nn.ConvTranspose2d(128, 64,  kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(64),  nn.LeakyReLU(0.2),
            nn.ConvTranspose2d(64,  32,  kernel_size=4, stride=2, padding=1),
            nn.BatchNorm2d(32),  nn.LeakyReLU(0.2),
            nn.ConvTranspose2d(32,  1,   kernel_size=4, stride=2, padding=1),
            nn.Tanh(),
        )

    # BUG-3 FIX: Original applied F.leaky_relu() to the FC projection before
    # reshape. Large positive values produced by LeakyReLU were passed to the
    # deconv stack whose final Tanh must heavily saturate them, killing gradients.
    # Fix: pass the FC output directly — no activation before reshape.
    def forward(self, z):
        h = self.fc(z).view(-1, 256, 8, 8)   # removed: F.leaky_relu()
        return self.deconv(h)


class ConvVAE(nn.Module):
    def __init__(self, latent_dim=32):
        super().__init__()
        self.encoder    = ConvEncoder(latent_dim)
        self.decoder    = ConvDecoder(latent_dim)
        self.latent_dim = latent_dim

    def reparameterise(self, mu, logv):
        std = torch.exp(0.5 * logv)
        return mu + torch.randn_like(std) * std

    def forward(self, x):
        mu, logv = self.encoder(x)
        z        = self.reparameterise(mu, logv)
        return self.decoder(z), mu, logv

    def encode(self, x):
        mu, _ = self.encoder(x)
        return mu


class LyricsEncoder(nn.Module):
    """FC encoder: 384 → 256 → 128 → 32."""
    def __init__(self, input_dim=384, latent_dim=32):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(input_dim, 256), nn.BatchNorm1d(256), nn.LeakyReLU(0.2),
            nn.Linear(256, 128),       nn.BatchNorm1d(128), nn.LeakyReLU(0.2),
            nn.Linear(128, latent_dim),
        )

    def forward(self, x):
        return self.net(x)


