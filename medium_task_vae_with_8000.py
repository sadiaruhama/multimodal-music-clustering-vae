# BUG-4 FIX: Correct ELBO loss formulation
#
# ORIGINAL BUG:
#   recon = F.mse_loss(x_recon, x, reduction='mean')
#               → divides by B × C × H × W = 32 × 1 × 128 × 128 = 524 288
#   kl    = -0.5 * torch.mean(1 + logv - mu² - exp(logv))
#               → divides by B × latent_dim = 32 × 32 = 1 024
#
# The ~512× scale difference caused KL to dominate, driving posterior collapse:
# the encoder ignored the input and output N(0,I) for every sample, making all
# latents nearly identical. This is why Silhouette → 1.0 and n_clusters → 1.
#
# FIX: Use reduction='sum' then divide by batch size for both terms so that
# recon and KL are both normalised per-sample regardless of tensor dimensions.
def vae_loss_fn(x, x_recon, mu, logv, beta=1.0):
    B     = x.size(0)
    recon = F.mse_loss(x_recon, x, reduction='sum') / B
    kl    = -0.5 * (1 + logv - mu.pow(2) - logv.exp()).sum(dim=1).mean()
    return recon + beta * kl, recon, kl


# BUG-5 FIX (part 2): Cosine alignment loss instead of MSE
#
# ORIGINAL BUG:
#   lyr_align_loss = F.mse_loss(z_lyr, mu_audio.detach())
#
# MSE alignment pushes the lyrics encoder to copy mu_audio exactly in magnitude
# and direction. With only 5 (later 10) distinct targets out of 1000 samples,
# the lyrics encoder collapses to a near-constant output. The fixed loss below
# only aligns directions (angular similarity), preserving the relative geometry
# of the 10 distinct genre embeddings.
def lyrics_align_loss_fn(z_lyr, mu_audio):
    return (1.0 - F.cosine_similarity(z_lyr, mu_audio.detach(), dim=1)).mean()


