"""## 10. Extract MFCC features for GTZAN rows with available audio"""

import librosa
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import train_test_split
import joblib

MFCC_DIR = f"{PART2_DIR}/mfcc_gtzan500"
os.makedirs(MFCC_DIR, exist_ok=True)

N_MFCC = 40
SR = 22050
DURATION = 30

mfcc_records = []
failed_mfcc = []

audio_gtzan = gtzan_meta[gtzan_meta["has_audio"] == True].copy()

if len(audio_gtzan) == 0:
    print("WARNING: No GTZAN audio files are available. Skipping MFCC extraction.")
else:
    print("Extracting MFCC from available GTZAN audio files:", len(audio_gtzan))

    for idx, row in tqdm(audio_gtzan.iterrows(), total=len(audio_gtzan)):
        filepath = row["filepath"]
        filename = row["filename"]
        genre = row["genre_label"]

        try:
            y, sr = librosa.load(filepath, sr=SR, duration=DURATION)
            mfcc = librosa.feature.mfcc(y=y, sr=sr, n_mfcc=N_MFCC)
            mfcc_mean = mfcc.mean(axis=1)
            mfcc_std = mfcc.std(axis=1)
            mfcc_combined = np.concatenate([mfcc_mean, mfcc_std]).astype(np.float32)

            save_name = filename.replace(".au", ".npy").replace(".wav", ".npy").replace(".mp3", ".npy")
            save_path = os.path.join(MFCC_DIR, save_name)
            np.save(save_path, mfcc_combined)

            mfcc_records.append({
                "filename": filename,
                "filepath": filepath,
                "genre": genre,
                "mfcc_path": save_path
            })
        except Exception as e:
            failed_mfcc.append({
                "filename": filename,
                "error": str(e)
            })

