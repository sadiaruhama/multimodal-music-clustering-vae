"""## 11. Extract mel-spectrograms for GTZAN rows with available audio"""

SPEC_DIR = f"{PART2_DIR}/spectrograms_gtzan500"
os.makedirs(SPEC_DIR, exist_ok=True)

N_MELS = 128
HOP_LEN = 512
TARGET_LEN = 128

spec_records = []
failed_spec = []

if len(audio_gtzan) == 0:
    print("WARNING: No GTZAN audio files are available. Skipping spectrogram extraction.")
else:
    print("Extracting mel-spectrograms:", len(audio_gtzan))

    for idx, row in tqdm(audio_gtzan.iterrows(), total=len(audio_gtzan)):
        filepath = row["filepath"]
        filename = row["filename"]
        genre = row["genre_label"]

        try:
            y, sr = librosa.load(filepath, sr=SR, duration=DURATION)

            mel = librosa.feature.melspectrogram(
                y=y,
                sr=sr,
                n_mels=N_MELS,
                hop_length=HOP_LEN
            )
            mel_db = librosa.power_to_db(mel, ref=np.max)

            if mel_db.shape[1] >= TARGET_LEN:
                mel_fixed = mel_db[:, :TARGET_LEN]
            else:
                pad_width = TARGET_LEN - mel_db.shape[1]
                mel_fixed = np.pad(
                    mel_db,
                    ((0, 0), (0, pad_width)),
                    mode="minimum"
                )

            mel_fixed = mel_fixed.astype(np.float32)

            save_name = filename.replace(".au", ".npy").replace(".wav", ".npy").replace(".mp3", ".npy")
            save_path = os.path.join(SPEC_DIR, save_name)
            np.save(save_path, mel_fixed)

            spec_records.append({
                "filename": filename,
                "filepath": filepath,
                "genre": genre,
                "spec_path": save_path,
                "shape": str(mel_fixed.shape)
            })
        except Exception as e:
            failed_spec.append({
                "filename": filename,
                "error": str(e)
            })

