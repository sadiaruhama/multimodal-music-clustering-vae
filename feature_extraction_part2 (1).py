"""## 3. Helper functions"""

def find_first_existing(paths):
    for p in paths:
        if p and os.path.exists(p):
            return p
    return None


def clean_generated_lyrics(text):
    # Basic text cleaning for Whisper pseudo-lyrics.
    if pd.isna(text):
        return ""
    text = str(text)
    text = re.sub(r"\n+", " ", text)
    text = re.sub(r"\s+", " ", text)
    text = text.strip()
    return text


def word_count(text):
    text = clean_generated_lyrics(text)
    if not text:
        return 0
    return len(text.split())


def is_corrupted_or_low_quality(text, min_words=5, max_repetition_ratio=0.65):
    # Flags empty, too-short, or highly repetitive transcripts.
    text = clean_generated_lyrics(text)
    words = text.lower().split()

    if len(words) < min_words:
        return True

    unique_ratio = len(set(words)) / max(len(words), 1)
    repetition_ratio = 1 - unique_ratio

    if repetition_ratio > max_repetition_ratio:
        return True

    bad_phrases = [
        "thank you",
        "thanks for watching",
        "applause",
        "music",
        "instrumental"
    ]
    low_text = text.lower()
    if len(words) <= 8 and any(p in low_text for p in bad_phrases):
        return True

    return False


def make_safe_title_from_filename(filename):
    if pd.isna(filename):
        return None
    name = os.path.basename(str(filename))
    name = re.sub(r"\.(au|wav|mp3|flac)$", "", name, flags=re.IGNORECASE)
    return name


def ensure_columns(df, required_cols, name="dataframe"):
    missing = [c for c in required_cols if c not in df.columns]
    if missing:
        raise ValueError(f"{name} is missing required columns: {missing}. Found columns: {df.columns.tolist()}")

